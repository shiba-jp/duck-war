function getEnemySpeed () {
    enemySpeedInit = -100
    if (game.runtime() < 20000) {
        enemySpeedInit = -100
    }
    if (game.runtime() >= 20000 && game.runtime() < 40000) {
        enemySpeedInit = -130
    }
    if (game.runtime() >= 40000 && game.runtime() < 60000) {
        enemySpeedInit = -160
    }
    if (game.runtime() >= 60000) {
        enemySpeedInit = -190
    }
    return enemySpeedInit
}
